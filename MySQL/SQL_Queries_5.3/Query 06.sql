SELECT * FROM EMPLOYEES;
SELECT * FROM ORDERSQ6;

CREATE TABLE ORDERSQ6 (
    ORDER_ID INT PRIMARY KEY,
    EMPLOYEE_ID INT,
    ORDER_DATE DATE,
    TOTAL_AMOUNT DECIMAL(10, 2),
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID)
);

